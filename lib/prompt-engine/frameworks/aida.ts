export function aidaSystemFragment(): string {
  return `FRAMEWORK: Attention-Interest-Desire-Action (AIDA)

Structure your output in this order:
1. Attention: Open with a hook directly relevant to the prospect's world. Not a compliment, not "I hope" — a statement or observation that makes them read on.
2. Interest: Build interest by connecting your offer to a concrete outcome they care about.
3. Desire: Sharpen desire with specificity — a result, a timeframe, a number, or a differentiator.
4. Action: End with a single, low-friction call to action.`;
}
