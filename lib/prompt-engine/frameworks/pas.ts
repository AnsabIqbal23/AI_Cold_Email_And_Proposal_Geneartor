export function pasSystemFragment(): string {
  return `FRAMEWORK: Problem-Agitation-Solution (PAS)

Structure your output in this order:
1. Problem: Name the prospect's specific problem concisely and accurately.
2. Agitation: Briefly sharpen why it hurts — the cost, the frustration, the consequence. One or two sentences at most.
3. Solution: Present the offer as the direct resolution. Be specific, not vague.`;
}
